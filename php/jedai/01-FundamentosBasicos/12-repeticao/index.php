  //laço de repetição for
  for ($cont =0;$cont<10;$cont++)
  echo "Ola mundo FOR $cont <hr>";

  //laço de repetição while
  $cont1 =0;
  while ($cont1 < 10){
    echo "Ola mundo WHILE $cont1 <hr>";
